#!/usr/bin/python3

def common_elements(set_1, set_2):
    """Get only the element(s) common to two sets

    Args:
        set_1: The fisrt set
        set_2: The second set

    Returns:
        Returns a list containing only the common element
    """

    return (set_1 & set_2)


if __name__ == '__main__':

    common_elements()
